
public class GradeAvg {

	public static void main(String[] args) {
		int quizScore1;
		quizScore1 = 88;
		int quizScore2;
		quizScore2 = 98;
		int quizScore3;
		quizScore3 = 78;
		int testScore1;
		testScore1 = 92;
		int testScore2;
		testScore2 = 82;
		int testScoreAvg;
		testScoreAvg = ((testScore1 + testScore2)/2);
		int quizScoreAvg;
		quizScoreAvg = ((quizScore1 + quizScore2 + quizScore3)/3);
		System.out.print("Test score average is ");
		System.out.println(testScoreAvg);
		System.out.print("Quiz score average is ");
		System.out.print(quizScoreAvg);
		
	}
}
